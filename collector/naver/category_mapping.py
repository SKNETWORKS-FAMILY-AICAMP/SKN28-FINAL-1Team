def normalize(name: Optional[str]) -> str:
    """카테고리명 비교용 정규화: 공백 제거."""
    return (name or "").replace(" ", "").strip()


# 정규화 키로 재구성 (대소문자/공백 변형 대응)
_CAT3 = {normalize(k): v for k, v in NAVER_CATEGORY3_MAP.items()}
_CAT4 = {normalize(k): v for k, v in NAVER_CATEGORY4_MAP.items()}


def is_fashion_item(category1: Optional[str]) -> bool:
    """category1 기준 패션 상품 여부. 검색 노이즈(생활용품 등) 필터."""
    return normalize(category1) in {normalize(c) for c in ALLOWED_CATEGORY1}


def map_naver_category(
    category1: Optional[str],
    category2: Optional[str],
    category3: Optional[str],
    category4: Optional[str],
) -> Tuple[Optional[str], Optional[str]]:
    """
    네이버 category1~4를 문서 (대분류, 소분류)로 매핑한다.

    category4가 category3보다 구체적이므로 category4 매핑을 먼저 시도하고,
    없으면 category3 매핑을 쓴다. 둘 다 실패하면 (None, None).
    """
    if not is_fashion_item(category1):
        return None, None

    hit = _CAT4.get(normalize(category4))
    if hit:
        return hit

    hit = _CAT3.get(normalize(category3))
    if hit:
        return hit

    return None, None
