# 성별 프리픽스 확장이 의미 없는 대분류 (이미 성별 중립적이거나 키워드에 성별 포함)
_NO_GENDER_EXPAND_LARGE = {"가방", "액세서리", "언더웨어/이너웨어"}


def iter_keywords(
    category_large: Optional[str] = None,
    category_small: Optional[str] = None,
    only_keyword: Optional[str] = None,
) -> Iterator[KeywordEntry]:
    """
    수집 대상 키워드를 순회한다.

    - category_large / category_small: 해당 분류만 수집할 때 필터
    - only_keyword: 단일 키워드 테스트용. KEYWORD_MAP에 없으면 분류 미상으로 반환.
    """
    if only_keyword:
        for (large, small), words in KEYWORD_MAP.items():
            if only_keyword in words:
                yield KeywordEntry(only_keyword, large, small, False)
                return
        yield KeywordEntry(only_keyword, "미분류", "미분류", False)
        return

    for (large, small), words in KEYWORD_MAP.items():
        if category_large and large != category_large:
            continue
        if category_small and small != category_small:
            continue
        gender_expand = large not in _NO_GENDER_EXPAND_LARGE
        for word in words:
            yield KeywordEntry(word, large, small, gender_expand)
            if gender_expand and KEYWORD_GENDER_PREFIXES:
                for prefix in KEYWORD_GENDER_PREFIXES:
                    yield KeywordEntry(f"{prefix} {word}", large, small, True)


def count_keywords(**kwargs) -> int:
    return sum(1 for _ in iter_keywords(**kwargs))
